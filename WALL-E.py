def bezierCurveLINE(p0,p1,p2,t):
    glColor3f(0.2709, 0.2948, 0.43)
    glBegin(GL_LINE_STRIP)
    pFinal=[]
    for t in np.arange(0,1,.1):
        
        pFinal.append(math.pow(1-t,2)*p0[0] + (1-t)*2*t*p1[0] + t*t*p2[0])
        pFinal.append(math.pow(1-t,2)*p0[1] + (1-t)*2*t*p1[1] + t*t*p2[1])
        glVertex(pFinal[0],pFinal[1])
        pFinal=[]   
    glEnd()
