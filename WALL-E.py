def draw_circle(r=0.5, xc=0, yc=0,l=0,h=2):
    glBegin(GL_POINTS)
    for theta in np.arange(l*math.pi, h*math.pi, 0.001):
        x = xc + r * math.cos(theta)
        y = yc + r * math.sin(theta)

        glVertex(x, y)
    glEnd()

def draw_circlePOLY(r=.02,xc=.3,yc=-.75,l=0,h=2):
    glPointSize(4)
    glBegin(GL_POLYGON)
    for theta in np.arange(l,h*math.pi,.001):
        ###f

        x=xc+r*math.cos(theta)
        
        y=yc+r*math.sin(theta)
        glVertex(x,y)        
        
    glEnd()
